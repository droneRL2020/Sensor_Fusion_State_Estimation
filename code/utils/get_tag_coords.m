function [p0, p1, p2, p3, p4] = get_tag_coords(id)
% @output   [p0, p1, p2, p3, p4]  2x5 xy coordinates of the tag[id]

% tag_ids =   [0, 12, 24, 36, 48, 60, 72, 84,  96;
%              1, 13, 25, 37, 49, 61, 73, 85,  97;
%              2, 14, 26, 38, 50, 62, 74, 86,  98;
%              3, 15, 27, 39, 51, 63, 75, 87,  99;
%              4, 16, 28, 40, 52, 64, 76, 88, 100;
%              5, 17, 29, 41, 53, 65, 77, 89, 101;
%              6, 18, 30, 42, 54, 66, 78, 90, 102;
%              7, 19, 31, 43, 55, 67, 79, 91, 103;
%              8, 20, 32, 44, 56, 68, 80, 92, 104;
%              9, 21, 33, 45, 57, 69, 81, 93, 105;
%             10, 22, 34, 46, 58, 70, 82, 94, 106;
%             11, 23, 35, 47, 59, 71, 83, 95, 107];

side = 0.152;
interval = 0.152;
interval34_78 = 0.178;

i = mod(id, 12);
j = floor(id/12);
assert(i>=0&i<12&j>=0&j<9);
p4 = [(side + interval)*i;(side+interval)*j];
if j >= 3 & j < 6
    p4 = p4 + [0; interval34_78-interval];
elseif j >= 6 
    p4 = p4 + [0; (interval34_78-interval)*2];
end
p1 = p4 + [side;0];
p2 = p4 + [side; side];
p3 = p4 + [0; side];
p0 = p4 + [side/2; side/2];
